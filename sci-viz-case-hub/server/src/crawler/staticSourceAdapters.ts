export function getStaticSourceAdapter(url: string): StaticSourceAdapter | null {
  let hostname = '';
  try {
    hostname = new URL(url).hostname;
  } catch {
    return null;
  }
  return STATIC_SOURCE_ADAPTERS.find(adapter =>
    adapter.hostPatterns.some(pattern => pattern.test(hostname))
  ) || null;
}
